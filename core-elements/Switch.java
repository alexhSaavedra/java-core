public class Switch{
    public static void main(String[] args) {
        
        int n = 7;

        switch (n) {
            case 2:
                System.out.println("2");
                break;
            case 3:
                System.out.println("3");
              break;
            case 1:
                System.out.println("1");
               break;    
            default:
                System.out.println("anything else");
                break;
        }
    }
}